import UIKit

extension UIColor {
    
    class func overlayColor() -> UIColor {
        return UIColor(red: 0/255.0, green: 0/255.0, blue: 0/255.0, alpha: 0.5)
    }
    
    class func backgroundColor() -> UIColor {
        return UIColor(red: 126/255.0, green: 200/255.0, blue: 219/255.0, alpha: 1.0)
    }
    
    class func cloudColor() -> UIColor {
        return UIColor(red: 255/255.0, green: 255/255.0, blue: 255/255.0, alpha: 0.4)
    }
    
}